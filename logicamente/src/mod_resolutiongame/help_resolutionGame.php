<h1>Resolution Game</h1>
<p>Para usar o <strong>ResolutionGame</strong> &eacute; preciso primeiro preencher o formul&aacute;rio com as cláusulas a partir das quais ser&aacute; feita a deriva&ccedil;&atilde;o. Para adicionar mais cl&aacute;usulas, basta clicar em <em>"Add more clauses"</em>, e para remover a &uacute;ltima cl&aacute;usula adicionada, deve-se clicar em <em>"Remove clause"</em>.</p> <p>Exemplo de cl&aacute;usulas:</p>
	<ul>
		<li>a | b</li>
		<li>~a</li>
		<li>~b</li>
	</ul>
<p>Clique em <em>"Start game"</em> para come&ccedil;ar o jogo. Nesse momento, as cl&aacute;usulas passaram para a &aacute;rea intitulada <em>"Clauses List"</em>, e tem-se duas op&ccedil;&otilde;es:</p>
	<ul>
		<li><em>"Solve automatically"</em>: o programa tenta fazer a deriva&ccedil;&atilde;o automaticamente</li>
		<li><em>"Do selected elimination"</em>, que o usu&aacute;rio pode utilizar ao selecionar duas cl&aacute;usulas que possuam literais complementares.</li>
	</ul>
<p>A qualquer momento &eacute; poss&iacute;­vel resetar o jogo clicando no bot&atilde;o <em>"Reset game"</em>, ou inserindo novas cl&aacute;usulas e clicando em <em>"Start game"</em>.</p>

