<h1>Formula Reader</h1>
<p>Para usar alguns m&oacute;dulos do logicamente &eacute; necess&aacute;rio passar a express&atilde;o para a &aacute;rea de transfer&ecirc;ncia (<em>&quot;Transfer area&quot;</em>) para fazer isso &eacute; preciso usar o <strong>m&oacute;dulo Formula Reader</strong>.</p>
<p>Para utiliz&aacute;-lo &eacute; preciso primeiro preencher o formul&aacute;rio com a express&atilde;o que ser&aacute; passada&agrave; &aacute;rea de transfer&ecirc;ncia. As express&otilde;es podem ser, ou n&atilde;o, parentetizadas. Os &aacute;tomos devem ser constitu&iacute;­dos de uma sequ&ecirc;ncia de letras que pode ser seguida de uma sequ&ecirc;ncia de n&uacute;meros. Para adicionar a express&atilde;o &agrave; &aacute;rea de transfer&ecirc;ncia, basta clicar em <em>"Formula Reader"</em>.</p> 
<p>Exemplo de express&otilde;es:</p>
	<ul>
		<li>a --&gt; (b | c)</li>

		<li>p3 &amp; p4 &lt;-&gt; p5</li>
		<li>joao | maria</li>
	</ul>
<p>Clique em cima da express&atilde;o, j&aacute; na &aacute;rea de transfer&ecirc;ncia, para uitliz&aacute;-la em outro m&oacute;dulo. Caso n&atilde;o queira mais utilizar a express&atilde;o a mesma pode ser deletada da &aacute;rea de transfer&ecirc;ncia clicando no bot&atilde;o <em>"Delete"</em>.</p>	

