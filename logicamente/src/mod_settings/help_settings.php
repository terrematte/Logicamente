<h1>Settings</h1>
<p>No <strong>Settings</strong> voc&ecirc; poder&aacute; definir conectivos para utilizar com os m&oacute;dulos do <strong>Logicamente</strong>.</p>
<p>Em <em>Formula Style</em>, escolha qual o formato de exibi&ccedil;&atilde;o das f&oacute;rmulas.</p>
<p>Para utilizar os conectivos, escolha os que est&atilde;o na lista <em>UnSelected</em> e clique na seta para a direita. Todos os conectivos contidos na lista <em>Selected</em> ser&atilde;o utilizados.</p>
<p>Para acrescentar um novo conectivo, clique em <em>Add New Connective</em>, digite o simbolo do conectivo em <strong>Symbol</strong>, sua aridade em <strong>Arity</strong> e clique em <em>Add</em>.</p>
<p>Quando concluir, clique em <em>Done</em>.</p>

